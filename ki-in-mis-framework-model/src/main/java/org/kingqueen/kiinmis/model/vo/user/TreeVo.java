package org.kingqueen.kiinmis.model.vo.user;

/**
 * @ClassName TreeVo
 * @description 树的实体类
 * @author wzq
 * @date 2017年12月2日
 * @version v1.0
 */
public class TreeVo {
	private String id;
	private String text;
	private String state;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	
	
}
